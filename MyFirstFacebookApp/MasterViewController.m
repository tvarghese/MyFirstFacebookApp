}
- (void)viewWillAppear:(BOOL)animated{
    self.relevantFeedStories = [(TabController*)[[self parentViewController] parentViewController] relevantFeed];
}
- (void)viewDidLoad
{
    [super viewDidLoad];
	// Do any additional setup after loading the view, typically from a nib.
    self.navigationItem.leftBarButtonItem = self.editButtonItem;

//    UIBarButtonItem *addButton = [[UIBarButtonItem alloc] initWithBarButtonSystemItem:UIBarButtonSystemItemAdd target:self action:@selector(insertNewObject:)];
//    self.navigationItem.rightBarButtonItem = addButton;
    // Initialize Refresh Control
   
    UIRefreshControl *refreshControl = [[UIRefreshControl alloc] init];
    NSAttributedString *attrString = [[NSAttributedString alloc] initWithString:@"Release to Refresh"];
    [refreshControl setAttributedTitle:attrString];
    // Configure Refresh Control
    [refreshControl addTarget:self action:@selector(refresh:) forControlEvents:UIControlEventValueChanged];
    // Configure View Controller
    [self setRefreshControl:refreshControl];
    self.detailViewController = (DetailViewController *)[[self.splitViewController.viewControllers lastObject] topViewController];
}

